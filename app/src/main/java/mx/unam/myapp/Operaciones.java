public class Operaciones {

    public int suma(int num1, int num2){
        int res;
        res=num1+num2;
        return res;
    }

    public int resta(int num1, int num2){
        int res;
        res=num1-num2;
        return res;
    }

    public int multiplica(int num1, int num2){
        int res;
        res=num1*num2;
        return res;
    }

    public int divide(int num1, int num2){
        int res;
        res=num1/num2;
        return res;
    }
}
